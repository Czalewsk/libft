#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		n;

	n = ft_strlen(s);
	while (n >= 0 && s[n] != (char)c)
		n--;
	return ((s[n] == (char)c) ? (char*)(&s[n]) : NULL);
}
