#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int n_cp;
	int i;

	i = 1;
	n_cp = n;
	while (!(n_cp > (-10) && n_cp < 10))
	{
		n_cp /= 10;
		i *= 10;
	}
	if (n < 0)
		ft_putchar_fd('-', fd);
	else
		n *= -1;
	while (i >= 1)
	{
		ft_putchar_fd(-((n / i) % 10) + 48, fd);
		i /= 10;
	}
}
