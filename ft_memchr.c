#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n && n != 0)
		if ((UC)c == ((UC*)s)[i++])
			return ((void*)&s[--i]);
	return (NULL);
}
