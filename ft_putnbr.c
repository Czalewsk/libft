#include "libft.h"

void	ft_putnbr(int n)
{
	int n_cp;
	int i;

	i = 1;
	n_cp = n;
	while (!(n_cp > (-10) && n_cp < 10))
	{
		n_cp /= 10;
		i *= 10;
	}
	if (n < 0)
		ft_putchar('-');
	else
		n *= -1;
	while (i >= 1)
	{
		ft_putchar(-((n / i) % 10) + 48);
		i /= 10;
	}
}
